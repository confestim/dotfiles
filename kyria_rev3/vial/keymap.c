bool oled_task_user(void) {
    uint8_t layer = get_highest_layer(layer_state);
    if (is_keyboard_left()) {
        // Left side: logo (pi by default, game logos on their layers)
        if (layer == _CSGO) {
            oled_write_raw_P((const char *)cs2_logo, sizeof(cs2_logo));
        } else if (layer == _LEAGUE) {
            oled_write_raw_P((const char *)league_logo, sizeof(league_logo));
        } else {
            oled_write_raw_P((const char *)pi_logo, sizeof(pi_logo));
        }
    } else {
        // Right side: all layers listed, arrow on active one
        oled_write_P(layer == _BASE     ? PSTR("> Base\n")       : PSTR("  Base\n"),       false);
        oled_write_P(layer == _NAV      ? PSTR("> Nav\n")        : PSTR("  Nav\n"),        false);
        oled_write_P(layer == _LEAGUE   ? PSTR("> League\n")     : PSTR("  League\n"),     false);
        oled_write_P(layer == _FUNCTION ? PSTR("> Function\n")   : PSTR("  Function\n"),   false);
        oled_write_P(layer == _CSGO     ? PSTR("> CS:GO\n")      : PSTR("  CS:GO\n"),      false);
        oled_write_P(layer == _SYM      ? PSTR("> Sym\n")        : PSTR("  Sym\n"),        false);
        oled_write_P(layer == _ADJUST   ? PSTR("> Adjust\n")     : PSTR("  Adjust\n"),     false);
    }
    return false;
}
